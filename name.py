name =input("이름을 입력하시오:") #사용자로부터 이름을 입력받아 변수'name'에 저장된다.
print(name,"씨 안녕하세요?") #변수'name'의 값과 문자열을 함께 출력한다.
print("파이썬에 오신 것을 환영합니다.") #"파이썬에 오신 것을 환영합니다." 문자열을 출력한다.

num1=int(input("첫 번째 정수를 입력하시오:")) #사용자로부터 정수를 입력받아 변수 'num1'에 저장된다.
num2=int(input("두 번째 정수를 입력하시오:")) #사용자로부터 정수를 입력받아 변수 'num2'에 저장된다.
sum=num1+num2 #변수 'num1','num2'에 저장된 값의 합을 구하여 변수 'sum'에 저장한다.

print(num1,"과",num2,"의 합은",sum,"입니다.") #변수 'num1'과 'num2'에 저장된 값과 'sum' 변수에 저장된 값을 출력한다.
